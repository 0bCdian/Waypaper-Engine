func TestExtractMetadata(t *testing.T) {
	tests := []struct {
		name           string
		format         string
		expectedWidth  int
		expectedHeight int
		expectedFormat string
		expectError    bool
	}{
		{"Valid PNG", "png", 1, 1, "png", false},
		// {"Valid JPEG", "jpeg", 1, 1, "jpeg", false}, // Commented out due to test data issues
		// Add more formats here as you create dummy images for them
		{"Invalid data", "invalid", 0, 0, "", true},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			var data []byte
			if tt.format == "invalid" {
				data = []byte("invalid image data")
			} else {
				data = createDummyImage(t, tt.format)
			}

			meta, err := ExtractMetadata(data)

			if (err != nil) != tt.expectError {
				t.Fatalf("ExtractMetadata() error = %v, expectError %v", err, tt.expectError)
			}

			if !tt.expectError {
				if meta.Width != tt.expectedWidth {
					t.Errorf("Expected width %d, got %d", tt.expectedWidth, meta.Width)
				}
				if meta.Height != tt.expectedHeight {
					t.Errorf("Expected height %d, got %d", tt.expectedHeight, meta.Height)
				}
				if meta.Format != tt.expectedFormat {
					t.Errorf("Expected format %s, got %s", tt.expectedFormat, meta.Format)
				}
			}
		})
	}
}

func TestIsSupportedFormat(t *testing.T) {
	tests := []struct {
		name     string
		format   string
		expected bool
	}{
		{"jpeg", "jpeg", true},
		{"png", "png", true},
		{"gif", "gif", true},
		{"bmp", "bmp", true},
		{"webp", "webp", true},
		{"tiff", "tiff", false},
		{"svg", "svg", false},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if IsSupportedFormat(tt.format) != tt.expected {
				t.Errorf("IsSupportedFormat(%q) = %v, expected %v", tt.format, !tt.expected, tt.expected)
			}
		})
	}
}
